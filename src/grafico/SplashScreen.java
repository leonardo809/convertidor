    private void imagen(){
        ImageIcon logo=new ImageIcon(getClass().getResource("/imagenes/logo.png"));
        Icon imagen1=new ImageIcon(logo.getImage().getScaledInstance(lbl_logo.getWidth(), lbl_logo.getHeight(), Image.SCALE_DEFAULT));
        lbl_logo.setIcon(imagen1);
        lbl_logo.setOpaque(false);
        this.repaint();
    }
    private void sonido()
    {
        AudioClip sonido;
        sonido= java.applet.Applet.newAudioClip(getClass().getResource("/audio/40y20.wav"));
        sonido.play();
    }    
    private void cursores(){
        Cursor cursor;
        ImageIcon imagen=new ImageIcon(getClass().getResource("/cursores/cflecha.png"));
        Toolkit t=Toolkit.getDefaultToolkit();
        cursor=t.createCustomCursor(imagen.getImage(), new Point(1,1), "Cursor");
        setCursor(cursor);
    }
    
